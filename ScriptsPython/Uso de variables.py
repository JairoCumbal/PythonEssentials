#Uso de Variables

num1=39 #Variable num1 con valor entero
num2=13 #Variable num2 con valor entero
suma=num1+num2 #Variable suma guarda el resultado de num1+num2
print(suma) #Impresión de variable suma
print("\n"*2) #Salto de línea (x2)
div=num1/num2 #Variable div guarda el resultado de num1/num2
print(div) #Impresión de la variable div
divint=num1//num2 #Variable divint guarda el resultado en enteros de num1/num2
print(divint) #Impresión de la variable divint
x=5 #Declaro una variable entera
letra="Python " #Declaro una variable string
resultado=letra*x #Operación con las variables x y letra se guarda en variable resultado
print(resultado) #Impresión de la variable resultado